export const baseExp = (level: number, diff: Difficulty) => {
  if (level < 1 || 10 < level) {
    throw new Error(`index out of range lelvel=${level}`);
  }
  if (diff < 0 || 3 < diff) {
    throw new Error(`index out of range diff=${diff}`);
  }
  return BASE_EXP[level - 1][diff];
};
